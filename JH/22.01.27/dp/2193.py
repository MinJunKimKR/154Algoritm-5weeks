''''
N=1 : 1 (1가지)
N=2 : 10 (1가지)
N=3 : 101, 100 (2가지)
N=4 : 1010, 1000, 1001, (3가지)
N=5 : 10100, 10000, 10010, 10101, 10001 (5가지)
N=6 : 101000, 100000, 100100, 101010, 100010, 101001, 100001, 100101 (8가지)
'''

n = int(input())
dp = [0] * (n+1)

for i in range(1, n+1):
  if i == 1 or i ==2:
    dp[i] = 1
  else:
    dp[i] = dp[i-1] + dp[i-2]
print(dp[n])